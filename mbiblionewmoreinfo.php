  <a name="abnewinterviews"></a><h4>The Interviews: What we learned</h4>
  <p>We had the opportunity to interview over 150 K&ndash;12 mathematics curriculum decision-makers representing districts in eight states. These states&mdash;Colorado, Louisiana, Maine, New York, Ohio, Texas, Washington, and West Virginia&mdash;were carefully selected to investigate curriculum decision-making in a variety of state-adoption and open-territory states across the country. Districts participating in interviews within each state reflected a range of characteristics in terms of performance level, geographic region, percent of students in poverty, size, and instructional materials used.</p>
  <p>Our interviewees were, in most cases, the individuals in each district directly responsible for overseeing the selection of new mathematics textbooks. Interviewers sought to understand curriculum decision-making processes in various settings, to identify the decisions curriculum leaders make, and to identify the role various sources of information play in those decisions. Of particular interest was the curriculum leaders&rsquo; use of research, particularly in light of national calls for a broader perspective on the research needed to properly evaluate instructional materials in mathematics (National Research Council, 2004).<br>
    <br>
    In these interviews, we learned that curriculum leaders use research (e.g., books, journal articles) throughout the adoption process, especially in preparing themselves and selection committees for the upcoming adoption. However, leaders also reported they sometimes struggled to locate applicable and appropriate research and often relied on a mixture of materials gathered from state leaders, the Internet, and colleagues. Given the numerous responsibilities and tasks our interviewees handle on a daily basis, they also expressed frustration over the challenge of finding the time to engage in a more thorough search. These leaders spoke of the need to find relevant research in a centrally located, easily accessible place. Consequently, an important product of our current study is an annotated bibliography that serves to point curriculum leaders to relevant articles they might use in the process of selecting mathematics instructional materials.<br>
  </p>
  <h4><a name="abnewcriteria"></a><br>
    Criteria for Inclusion  </h4>
    <ul>
      <li>Relevance for curriculum leaders involved in materials selection or implementation</li>
      <li>Accessible for curriculum leaders (e.g., readability)</li>
      <li>Peer-reviewed</li>
      <li>Specific connection to instructional materials</li>
      <li>Published in 2000 or later</li>
    </ul>
  <p>In a few cases, slight exceptions were made to this list of criteria. For example, a handful of articles published prior to the year 2000, were identified as seminal pieces in the field, and therefore valuable resources for the selection and implementation of mathematics curricula. We also chose to include a handful of books in which the content is directly tied to instructional materials, as their absence would have resulted in an incomplete list. It should also be noted that other resources with specific links to instructional materials such as conference proceedings and presentations, doctoral dissertations, and newspaper articles, are available. Generally, these resources are not peer-reviewed and are difficult to comprehensively search and access.<br>
    <br>
  </p>
  <a name="abneworgstruct"></a>
    <h4>Organizational Structure</h4>
    <p>The articles in this bibliography are organized into three primary categories: preparation for selection and implementation, effectiveness studies, and implementation of instructional materials. We designed this organizing framework to mirror the phases of the adoption process, during which curriculum leaders typically begin by preparing their committee to evaluate materials, the committees evaluate the quality of the materials, and after a selection decision, districts then arrange for implementation. Articles in the preparation category are generally those perceived to be helpful to curriculum leaders as they ready themselves and their committee for an examination of potential materials. The effectiveness studies category includes pieces that largely look at student achievement results for specific textbooks. The final set of articles grouped under implementation of instructional materials includes articles that address issues that arise when considering the implementation of newly adopted materials, including providing professional development. <br>
      <br>
      Other organizational structures would have been possible, but we felt this representation addressed the needs expressed by our interviewees. It should also be noted that in more than one instance an article fit reasonably well into more than one category. However, we have chosen to take a best-fit approach, and articles only appear once throughout the annotated bibliography.<br></p>
      <br>
      <a name="abnewreltop"></a>    
    <h4>Relevant Topics</h4>
      <p>This bibliography does not attempt to be comprehensive and certainly does not include every article written about mathematics curriculum or related relevant issues, such as how children learn mathematics or best teaching practices. (You will find articles on these topics when specifically connected to instructional materials.) In fact, if comprehensively and exhaustively searched, each of these topics would be worthy of their own annotated bibliography. Rather, the goal was to increase curriculum leaders&rsquo; understanding of topics relevant to the selection and implementation of mathematics instructional materials.<br></p>
        <br>
        <a name="abnewperm"></a>      
      <h4>Permissions</h4>
      <p>An abstract is provided with each article, which in most cases, was written by the original author or publishers. These abstracts are reprinted with permission from the publishers. Some publishers requested we include a link to their website, or in a few cases, a direct link to the article, as a condition of granting permission. Occasionally, articles were selected that did not include an abstract. For these items, we have written an abstract. Abstracts were also written if we were unable to secure permission from the publisher.<br>
