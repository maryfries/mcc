                <h4><span class="bold"><a href="use.php">Effective Use of Mathematics Instructional Materials</a></span></h4>
                <p>Nearing completion, this applied research study sought to investigate mathematics textbook selection, with particular attention to the decisions curriculum leaders make as they navigate the selection process and the key factors affecting these decisions. Given this era of accountability it is not surprising that state standards and tests were highly influential in crafting an initial set of materials for consideration. Additionally, we found that the context in which decisions are made is also critical. However, there are many openings in the process for curriculum leaders to influence the process. <a href="use.php">Learn more about this textbook selection project.</a></p>
