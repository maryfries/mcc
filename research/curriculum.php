   <h4>The Mathematics &nbsp;Instructional Materials Study at EDC</h4>
   <br>
   <p><strong>Introduction</strong><br>
Many districts across the country have centered their recent mathematics improvement efforts on the use of instructional materials aligned to state standards, often in response to increased accountability for student outcomes. Supporting this movement is the emergence of well-designed instructional materials, including programs such as <em>Everyday Mathematics</em> and <em>Investigations in Number, Data and Space</em>. As districts adopt these materials, they may design implementation plans to assist teachers and administrators in using the instructional materials effectively and in a manner that aligns with district goals. The <em>Mathematics Instructional Materials Study</em> (<em>MIM) </em>will examine the various types of supports available for teachers as they implement these new mathematics instructional materials. The results of the <em>MIM </em>study will assist district and school administrators and policy-makers in planning effective implementation of mathematics instructional materials.</p>
   <p><strong>Research design</strong><br>
     The <em>MIM</em> study is a four-year, longitudinal study of 10-12 school districts&rsquo; implementation of elementary mathematics instructional materials. We will study the relationships among the district level of coherence of implementation, the school level of support for implementation, the school level of use of materials, and the effects on student outcomes. Data will be gathered largely through interviews and surveys of district and school staff. Participation will be limited to those districts that have adopted <em>Everyday Mathematics</em> or<em> Investigations in Number, Data and Space</em>. </p>
   <p><strong>Project staff</strong><br>
     The co-Principal Investigators for the project are June Mark and Deborah Spencer, Senior Project Directors at Education Development Center (EDC). Both bring significant experience in mathematics education and curriculum implementation. Ms. Mark and Ms. Spencer are co-directors of the <em>K-12 Mathematics Curriculum Center</em>, which supports districts in making thoughtful, informed decisions about mathematics instructional materials, and also directed a qualitative research study of how districts make decisions about mathematics instructional materials and the factors that influence those decisions. Katherine Schwinden and Julie Zeringue add practical experience in classroom teaching as well as research experience on a range of qualitative studies. Dr. Jess Gropen and Dr. Stacy Ehrlich add extensive experience designing, conducting and analyzing quantitative research studies to the project team.</p>
   <p><strong>About Education Development Center, Inc. (EDC)</strong><br>
     The <em>MIM</em> project staff is located in Newton, Massachusetts. EDC is a global nonprofit organization that develops, delivers, and evaluates innovative programs to address some of the world&rsquo;s most urgent challenges in education, health, and economic development. All research activities involving human subjects are reviewed by EDC's Institutional Review Board (IRB) in accordance with the requirements of federal human subjects research regulations (45CFR46).</p>
