   <h1>Research</h1>
   <h4><img src="../Images/3squares.gif" width="100" height="35" class ="bigsquares" alt="Squares" title="Squares"><strong><br>
     <br>
     <br>
     Effective Use of Mathematics Instructional Materials: Building Bridges Between Research and Practice</strong>
     </h4>
   <br>
    <p>Knowing the influence instructional materials have in the classroom and the need to better understand the selection process, we are in the process of completing a study investigating curricular decision-making, with particular attention to the selection of mathematics instructional materials. The research questions guiding this work include:&nbsp; 
   </h4>
   <ul type="disc">
     <li>What processes do school districts use in selecting      and implementing mathematics curricula? What factors shape curriculum      decisions in K-12 mathematics?</li>
     <li>How does curriculum selection differ in      state-adoption and open-territory states? In what ways do state standards      and tests affect textbook decisions?&nbsp; </li>
     <li>What research do curriculum leaders use and find most      useful? How does research influence districts&rsquo; mathematics selection      decisions?</li>
     <li>What questions about mathematics instructional      materials do decision-makers need answered most?</li></ul>
  <p> In order to understand the complexities and realities of how districts select mathematics instructional materials, we interviewed 150 K&ndash;12 mathematics curriculum decision-makers from districts in eight states. The states&mdash;Colorado, Louisiana, Maine, New York, Ohio, Texas, Washington, and West Virginia&mdash;represent a mix of state-adoption states (in which the state provides a list of approved textbooks and a timeline for adoption) and open-territory states (in which the choice of textbooks and timeline for selection is unrestricted by the state) across the country. The districts we selected for interviews within each state reflect a range of characteristics in terms of performance level, geographic region, percent of students in poverty, size, and textbooks used.<p>Our interviewees were, in most cases, the person in each district directly responsible for overseeing the selection of new mathematics textbooks. Interviewers sought to understand curriculum decision-making processes in various settings, to identify the decisions curriculum leaders make, and to identify the role various sources of information play in those decisions. We were particularly interested in curriculum leaders&rsquo; use of research, in light of national calls for a broader perspective on the research needed to properly evaluate instructional materials in mathematics (National Research Council, 2004). <br>
     <br>
     The qualitative data gathered in these interviews has been supplemented by other sources, including a survey of the members of the Association of State Supervisors of Mathematics, a series of surveys of curriculum leaders nationally, an investigation of state-level documents and websites, and a review of the relevant literature. This data was analyzed first for each state individually to identify themes and hypotheses. We then looked across states to identify a set of claims in six key areas, including factors that influence mathematics textbook decisions and curriculum leaders&rsquo; use of research and resources. We then coded each interview, as well as the survey results, organizing the data available to confirm or disconfirm each possible claim. <br>
     <br>
     Findings from our data suggest five primary influences affecting K&ndash;12 curriculum decisions, including state standards and tests, committee evaluation of the quality of the instructional materials, data about instructional materials under consideration, teacher acceptance, and advocacy. For example, an overwhelming majority of curriculum leaders described the influence of state standards and tests on their selection process, reflecting greater accountability pressures. These pressures resulted in a move toward more centralized decision-making about mathematics instructional materials. In addition, nearly 80% of curriculum leaders also indicated using resources or research at some juncture of the process, including to frame the short list of materials for consideration, in preparation for making a final decision, or in gathering information to set the stage for the selection process. </p>
