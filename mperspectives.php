                <h2>Perspectives on Curricular Change: Interviews with Teachers, Administrators, and Curriculum Developers</h2>
                <p><span class="title">Perspectives on Curricular Change</span> is a collection of edited interviews with users of 12 National Science Foundation-funded, comprehensive mathematics curricula. Three books make up the series, providing perspectives from teachers, administrators, and developers at the elementary, middle, and high school levels.</p>
                <p><span class="title">Perspectives on Curricular Change</span> aims to provide readers with a better understanding of the ways in which <span class="title">Standards</span>-based curricula differ from traditional textbooks, and to offer a glimpse into the experiences of real people who have implemented these programs.</p>
                <p>Several common themes emerge from these stories, underscoring ways that using <span class="title">Standards</span>-based curricula leads schools and districts to view the teaching and learning of mathematics differently, to think strategically about implementation, to provide new types of support for teachers, and to build support among stakeholders.</p>
