  <h2>Selecting Mathematics Instructional Materials: An Annotated Bibliography</h2>
  <p>Access the annotated bibliography online in one of three ways:</p>
  <ul>
    <li><strong><a href="mbiblionewbrowse.asp">Browse</a></strong> the Annotated Bibliography by section</li>
    <li><strong><a href="mbiblionewlist.asp">Search</a></strong> the Annotated Bibliography by author</li>
    <li><strong><a href="K12MCCAB2009.pdf" target="_blank">Download a PDF</a></strong> of the complete Annotated Bibliography</li>
  </ul>
  <p><br>
    This publication, an annotated bibliography of articles relevant to the selection and implementation of mathematics instructional materials, is intended as a resource for mathematics curriculum leaders across all phases of the selection process. Whether preparing for an upcoming textbook adoption, actively involved in choosing materials, or preparing to launch the implementation of a recently selected text, this publication provides relevant information in an easily accessible format.<br>
    <br>
    This edition builds on the popular earlier editions of the Annotated Bibliography published by the K&ndash;12 MCC, but unlike earlier editions, the intent here is to focus specifically on mathematics curriculum leaders&rsquo; needs, and not limit the included articles to those solely linked to <em>Standards</em>-based curriculum reform. Instead we cast a wider net, so leaders would have a helpful resource regardless of whether the ultimate decision leads to an NSF-funded or commercially-developed set of materials. In gathering resources for this publication, the K&ndash;12 MCC staff reviewed articles from a comprehensive set of peer-reviewed journals and used an established set of criteria to determine inclusion. <br>
    <br>
    The articles in this bibliography are organized to mirror the phases of the adoption process and fall into three primary categories: preparation for selection and implementation, effectiveness studies, and implementation of instructional materials. This bibliography does not attempt to be comprehensive and certainly does not include every article written about mathematics curriculum or related relevant issues.  Each reference also includes an annotation to provide readers with a short description of each article. The links below provide further information about the development of the Annotated Bibliography. </p>
