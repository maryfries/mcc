  <h2>&quot;Go To&quot; Lists for curriculum selection and implementation</h2>
  <p>Making decisions regarding the selection and implementation of mathematics instructional materials can be an overwhelming task, especially when thoughtful decisions often need to be made quickly. Although the<a href="mbiblionew.asp"> Annotated Bibliography</a>  provides abstracts for over 100 articles related to the selection and implementation of materials, the K&ndash;12 MCC staff has put together a &ldquo;go to&rdquo; list of articles for each grade band (elementary, middle, high) that we feel represents seminal work in field, highlights the key topics around selection and implementation, and offers a jumping off point for those involved in decision-making. <br>
    <br>
    Download the &quot;go to&quot; lists for each grade band:</p>
